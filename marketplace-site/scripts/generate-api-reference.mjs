import { readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const siteRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const sourceRoot = path.resolve(siteRoot, "..", "runelite-client", "src", "main", "java",
  "net", "runelite", "client", "plugins", "klite");
const areas = [
  ["Client API", "api"],
  ["Automation runtime", "automation"],
  ["Web walker", "walker"]
];

function cleanJavaDoc(raw = "") {
  return raw
    .replace(/^\/\*\*|\*\/$/g, "")
    .split(/\r?\n/)
    .map((line) => line.replace(/^\s*\*\s?/, "").trim())
    .filter((line) => line && !line.startsWith("@"))
    .join(" ")
    .replace(/\{@code\s+([^}]+)}/g, "$1")
    .replace(/\{@link\s+([^}\s]+)(?:\s+([^}]+))?}/g, (_, target, label) => label || target)
    .replace(/<[^>]+>/g, "")
    .replace(/\s+/g, " ")
    .trim();
}

function typeDocumentation(source, declarationIndex) {
  const end = source.lastIndexOf("*/", declarationIndex);
  if (end < 0 || source.slice(end + 2, declarationIndex).trim()) {
    return "";
  }
  const start = source.lastIndexOf("/**", end);
  return start < 0 ? "" : cleanJavaDoc(source.slice(start, end + 2));
}

function signatures(source, isInterface) {
  const results = new Set();
  const pattern = /(?:^|\n)\s*(public\s+)?((?:<[^;{}]+>\s+)?[\w$.<>\[\], ?@]+\s+\w+\s*\([^;{}]*\)(?:\s+throws\s+[^;{]+)?)\s*[;{]/g;
  for (const match of source.matchAll(pattern)) {
    if (!match[1] && !isInterface) {
      continue;
    }
    const signature = match[2]
      .replace(/@[\w.]+(?:\([^)]*\))?\s*/g, "")
      .replace(/\s+/g, " ")
      .trim();
    if (!signature.includes("=")) {
      results.add(signature);
    }
  }
  return [...results].sort((left, right) => left.localeCompare(right));
}

async function buildReference() {
  const sections = [];
  let methodCount = 0;
  let typeCount = 0;
  for (const [name, directory] of areas) {
    const directoryPath = path.join(sourceRoot, directory);
    const files = (await readdir(directoryPath)).filter((file) => file.endsWith(".java")).sort();
    const types = [];
    for (const file of files) {
      const source = await readFile(path.join(directoryPath, file), "utf8");
      const packageName = source.match(/package\s+([\w.]+);/)?.[1];
      const declaration = /public\s+(?:(?:final|abstract|sealed)\s+)?(class|interface|enum|record)\s+(\w+)/.exec(source);
      if (!packageName || !declaration) {
        continue;
      }
      const methods = signatures(source, declaration[1] === "interface");
      methodCount += methods.length;
      typeCount += 1;
      types.push({
        name: declaration[2],
        kind: declaration[1],
        packageName,
        description: typeDocumentation(source, declaration.index),
        signatures: methods
      });
    }
    sections.push({ name, types });
  }
  return {
    schemaVersion: 1,
    scope: "KLite public client, automation, and web-walker API. Marketplace internals are excluded.",
    typeCount,
    methodCount,
    sections
  };
}

const reference = await buildReference();
const output = `// Generated by scripts/generate-api-reference.mjs. Do not edit manually.\n`
  + `export const API_REFERENCE = ${JSON.stringify(reference, null, 2)};\n`;
await writeFile(path.join(siteRoot, "worker", "api-reference.generated.js"), output, "utf8");
console.log(`Generated ${reference.typeCount} public types and ${reference.methodCount} signatures.`);
