import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDirectory = path.dirname(fileURLToPath(import.meta.url));
const publicDirectory = path.resolve(scriptDirectory, "../public");
const sourceFiles = [
  "styles.css",
  "filters.css",
  "admin.css",
  "site-shell.css",
  "release-preview.css",
  "obsidian-release.css",
  "final-obsidian.css",
  "preview-match.css",
  "credits.css",
  "resources-menu.css",
];

const sections = await Promise.all(sourceFiles.map(async (file) => {
  const source = await readFile(path.join(publicDirectory, file), "utf8");
  return `/* Source: ${file} */\n${source.trim()}\n`;
}));

const banner = "/* Generated by scripts/build-home-styles.mjs. Do not edit directly. */\n";
const outputPath = path.join(publicDirectory, "home.css");
const output = `${banner}${sections.join("\n")}`;

if (process.argv.includes("--check")) {
  const current = await readFile(outputPath, "utf8");
  if (current !== output) {
    throw new Error("public/home.css is stale. Run npm run css:build and commit the result.");
  }
} else {
  await writeFile(outputPath, output, "utf8");
}
